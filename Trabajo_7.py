#Programa que calcula perimetro y area de un rectangulo#
print("Programa que calcula perimetro y area de un rectangulo")

#variables de entrada#
Ancho= float(input("cual es el ancho del rectangulo?"))
Altura= float(input("Cual es la altura del rectangulo?"))

#Procedimiento y datos de salida
Perimetro= 2*(Ancho+Altura)
Area=(Ancho*Altura)

print("El area es de",Area , "y el perimetro es de",Perimetro)