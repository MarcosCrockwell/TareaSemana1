#Programa que calcula el area de un triangulo#
print("Programa que calcula el area de un triangulo")

#Variables de entrada#
Base=float(input("Cual es la base del triangulo?"))
Altura=float(input("Cual es la altura del triangulo?"))

#Procedimiento y datos de salida#
Area= (Base*Altura)/2
print("El area del triangulo es", Area)