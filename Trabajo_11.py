#Programa que calcula el area de un trapecio#
print("Programa que calcula el area de un trapecio")

#Datos de entrada#
Base1=float(input("ingrese la longitud de una base"))
Base2=float(input("ingrese la longitud de la otra base"))
Altura=float(input("ingrese la la altura del trapecio"))

#Procedimineto y datos de salida#
Area= ((Base1+Base2)*Altura)/2
print("El area del trapecio es de", Area)